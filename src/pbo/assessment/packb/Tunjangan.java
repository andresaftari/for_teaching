package pbo.assessment.packb;

public class Tunjangan {
    private int tnjgnDsr, tnjgnJbtn, tnjgnKes;

    public Tunjangan(int tnjgnDsr, int tnjgnJbtn, int tnjgnKes) {
        this.tnjgnDsr = tnjgnDsr;
        this.tnjgnJbtn = tnjgnJbtn;
        this.tnjgnKes = tnjgnKes;
    }

    public int getTnjgnDsr() {
        return tnjgnDsr;
    }

    public int getTnjgnJbtn() {
        return tnjgnJbtn;
    }

    public int getTnjgnKes() {
        return tnjgnKes;
    }
}
